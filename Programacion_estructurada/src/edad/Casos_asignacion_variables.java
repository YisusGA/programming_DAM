package edad;

public class Casos_asignacion_variables {

	public static void main(String[] args) {
		int edad;
		edad = 78;
		System.out.println(edad);
		//En la siguiente línea, estamos haciendo una reasignación de variable. No hay que volverla a declarar.
		edad = 47;
		System.out.println(edad);
	}

}
