package edad_mayor_menor;

public class Edad_mayor_menor {

	public static void main(String[] args) {
		int edadYisus;
		edadYisus = 31;
		int edadDani;
		edadDani = 27;
		
		if (edadYisus > edadDani) {
			System.out.println("Yisus es mayor");
		} else {
			System.out.println("Dani es mayor");
		}		
	}

}
