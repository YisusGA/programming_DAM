package expresion_con_operadores;

public class Declarar_e_inicializar_variables_en_la_misma_linea {

	public static void main(String[] args) {
		
		//Podemos declarar e incluso inicializar varias variables del mismo tipo en la misma línea, separándolas con ;
		int n1; int n2;
		n1 = 7;
		n2 = 8;
		
		int n3 = 7; int n4 = 8;
		
		//También podría hacerse esto
		int n5, n6;
		n5 = 1; 
		n6 = 2;
		//O esto
		int n7, n8;
		n7 = 1; n8 = 2;
		
	}

}
